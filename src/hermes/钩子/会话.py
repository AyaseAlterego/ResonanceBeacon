"""会话层钩子工厂"""
from typing import Optional
import logging

from .基础 import 钩子, 钩子层级, 钩子事件, 钩子上下文, 安全创建钩子

logger = logging.getLogger(__name__)

class 会话钩子工厂:
    """
    会话层钩子工厂

    创建与流水线生命周期相关的钩子（启动、完成、失败等）
    """

    def 创建钩子(self, 上下文: 钩子上下文) -> list[钩子]:
        """创建所有会话层钩子"""
        钩子列表 = [
            安全创建钩子(
                "流水线启动通知",
                self._创建流水线启动通知,
                上下文
            ),
            安全创建钩子(
                "流水线完成通知",
                self._创建流水线完成通知,
                上下文
            ),
            安全创建钩子(
                "流水线失败通知",
                self._创建流水线失败通知,
                上下文
            ),
            安全创建钩子(
                "阶段开始通知",
                self._创建阶段开始通知,
                上下文
            ),
            安全创建钩子(
                "阶段完成通知",
                self._创建阶段完成通知,
                上下文
            ),
            安全创建钩子(
                "任务开始通知",
                self._创建任务开始通知,
                上下文
            ),
            安全创建钩子(
                "任务完成通知",
                self._创建任务完成通知,
                上下文
            ),
        ]

        # 过滤掉None（创建失败的钩子）
        return [h for h in 钩子列表 if h is not None]

    def _创建流水线启动通知(self, 上下文: 钩子上下文) -> Optional[钩子]:
        """创建流水线启动通知钩子"""
        def 回调(流水线):
            名称 = 流水线.get("name", "未知") if isinstance(流水线, dict) else (流水线.名称 if hasattr(流水线, "名称") else "未知")
            logger.info(f"流水线启动: {名称}")

        return 钩子(
            名称="流水线启动通知",
            层级=钩子层级.会话,
            事件=钩子事件.流水线已启动,
            回调=回调,
            优先级=5,
            描述="记录流水线启动事件"
        )

    def _创建流水线完成通知(self, 上下文: 钩子上下文) -> Optional[钩子]:
        """创建流水线完成通知钩子"""
        def 回调(流水线, 结果):
            名称 = 流水线.get("name", "未知") if isinstance(流水线, dict) else (流水线.名称 if hasattr(流水线, "名称") else "未知")
