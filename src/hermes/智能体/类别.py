"""基于类别的智能体路由器"""
from typing import Optional
import logging

from .基础 import 智能体适配器, 智能体类别, 任务需求

logger = logging.getLogger(__name__)

class 类别路由器:
    """
    基于类别的智能体路由器

    不是直接指定使用哪个智能体，而是通过类别来描述任务需求，
    系统自动路由到最佳智能体。
    """

    def __init__(self):
        # 路由表：类别 -> 智能体ID列表（按优先级排序）
        self.路由表: dict[智能体类别, list[str]] = {
            智能体类别.超级大脑: ["claude_code"],
            智能体类别.深度: ["claude_code", "opencode"],
            智能体类别.快速: ["claude_code", "opencode"],
            智能体类别.视觉工程: ["claude_code"],
            智能体类别.专家: ["claude_code", "codex"],
            智能体类别.探索: ["claude_code", "opencode"],
            智能体类别.顾问: ["claude_code"],
            智能体类别.工具: ["claude_code", "opencode", "codex"],
        }
        # 智能体注册表（由外部注入）
        self._智能体注册表 = None

    def 设置注册表(self, 注册表):
        """设置智能体注册表"""
        self._智能体注册表 = 注册表

    def 注册类别路由(self, 类别: 智能体类别, 智能体ID列表: list[str]):
        """注册新的类别路由"""
        self.路由表[类别] = 智能体ID列表
        logger.info(f"注册类别路由: {类别.value} -> {智能体ID列表}")

    def 选择智能体(
        self,
        类别: 智能体类别,
        任务需求: 任务需求
    ) -> Optional[智能体适配器]:
        """
        根据类别和任务需求选择最佳智能体

        选择策略：
        1. 从路由表获取候选智能体列表
        2. 过滤健康且可用的智能体
        3. 根据任务需求进行评分和排序
        4. 返回最佳匹配
        """
        if self._智能体注册表 is None:
            raise RuntimeError("智能体注册表未设置")

        # 从路由表获取候选智能体ID列表
        候选ID列表 = self.路由表.get(类别, [])

        if not 候选ID列表:
            logger.warning(f"类别 {类别.value} 没有配置候选智能体")
            return None

        # 获取可用的智能体实例
        可用智能体 = []
        for 智能体ID in 候选ID列表:
            智能体 = self._智能体注册表.获取智能体(智能体ID)
            if 智能体 and self._是否健康(智能体):
                可用智能体.append(智能体)

        if not 可用智能体:
            logger.warning(f"类别 {类别.value} 没有可用的健康智能体")
            return None

        # 根据任务需求进行评分
        评分智能体列表 = []
        for 智能体 in 可用智能体:
            评分 = self._评分智能体(智能体, 任务需求)
            评分智能体列表.append((智能体, 评分))

        # 按评分降序排序
        评分智能体列表.sort(key=lambda x: x[1], reverse=True)

        # 返回最佳匹配
        最佳智能体 = 评分智能体列表[0][0]
        logger.info(
            f"为类别 {类别.value} 选择智能体: {最佳智能体.智能体ID} "
            f"(评分: {评分智能体列表[0][1]:.2f})"
        )

        return 最佳智能体

    def _是否健康(self, 智能体: 智能体适配器) -> bool:
        """检查智能体是否健康"""
        # 这里可以添加更复杂的健康检查逻辑
        # 例如：检查最近的成功率、响应时间等
        return True

    def _评分智能体(self, 智能体: 智能体适配器, 需求: 任务需求) -> float:
        """
        对智能体进行评分

        评分维度：
        1. 能力匹配度（权重：10）
        2. 上下文窗口匹配（权重：20）
        3. 成本（权重：-1/10）
        4. 负载均衡（权重：-15）
        5. 任务优先级（权重：5）
        """
        评分 = 0.0

        # 1. 能力匹配度
        智能体能力名称 = [c.名称 for c in 智能体.能力列表]
        能力匹配数 = len(set(需求.能力列表) & set(智能体能力名称))
        评分 += 能力匹配数 * 10

        # 2. 上下文窗口匹配
        最大上下文窗口 = max([c.上下文窗口 for c in 智能体.能力列表]) if 智能体.能力列表 else 0
        if 最大上下文窗口 >= 需求.预估令牌数:
            评分 += 20
        else:
            # 上下文窗口不足，扣分
            评分 -= (需求.预估令牌数 - 最大上下文窗口) / 1000

        # 3. 成本考虑（优先选择便宜的）
        if 智能体.能力列表:
            平均成本 = sum([c.每千令牌成本 for c in 智能体.能力列表]) / len(智能体.能力列表)
            评分 -= 平均成本 / 10

        # 4. 负载均衡（优先选择负载低的）
        # 这里需要从注册表获取负载信息
        # 暂时假设负载为0
        评分 -= 0 * 15

        # 5. 任务优先级
        评分 += 需求.优先级 * 5

        return 评分
