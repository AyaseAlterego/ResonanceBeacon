    def 获取模板(self, 模板名称: str) -> Optional[提示词模板]:
        """获取指定的提示词模板"""
        return self._模板.get(模板名称)

    def 生成提示词(
        self,
        模板名称: str,
        上下文: dict[str, Any]
    ) -> Optional[dict[str, str]]:
        """
        根据模板和上下文生成提示词

        Args:
            模板名称: 模板名称
            上下文: 上下文数据

        Returns:
            包含系统提示词和用户提示词的字典
        """
        模板 = self.获取模板(模板名称)
        if not 模板:
            logger.warning(f"未找到模板: {模板名称}")
            return None

        try:
            # 填充用户提示词模板
            用户提示词 = 模板.用户提示词模板.format(**上下文)

            return {
                "系统提示词": 模板.系统提示词,
                "用户提示词": 用户提示词
            }

        except KeyError as e:
            logger.error(f"模板填充失败，缺少键: {e}")
            return None
        except Exception as e:
            logger.error(f"生成提示词失败: {e}")
            return None

    def 根据任务类型获取模板(self, 任务类型: str) -> str:
        """根据任务类型获取对应的模板名称"""
        # 任务类型到模板的映射
        映射 = {
            "code_generation": "代码生成",
            "code_review": "代码审查",
            "test_generation": "测试生成",
            "documentation": "文档生成",
            "architecture": "架构设计",
            "requirements": "需求分析",
        }

        return 映射.get(任务类型, "代码生成")  # 默认使用代码生成模板

    def 获取所有模板(self) -> list[str]:
        """获取所有可用的模板名称"""
        return list(self._模板.keys())
